    private void montaTabela() {
        ArrayList<Produto> produtos = controlaProduto.recuperarTodos();
        if (produtos == null) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar Produto");
        } else {
            tblProduto.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "ID";
                        case 1:
                            return "Nome";
                        case 2:
                            return "Tipo";
                        case 3:
                            return "Peso";
                        case 4:
                            return "Ingrediente";
                        case 5:
                            return "Quantidade Estoque";
                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 6;
                }

                @Override
                public int getRowCount() {
                    return produtos.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Produto p = produtos.get(rowIndex);

                    if (p != null) {
                        switch (columnIndex) {
                            case 0:
                                return p.getId();
                            case 1:
                                return p.getNome();
                            case 2:
                                return p.getTipo();
                            case 3:
                                return p.getPeso();
                            case 4:
                                return p.getIngrediente();
                            case 5:
                                return p.getQtdestoque();
                        }

                    }

                    return "n/d";
                }
            });

            tblProduto.getColumnModel().getColumn(0).setMinWidth(80);
            tblProduto.getColumnModel().getColumn(0).setPreferredWidth(80);
            tblProduto.getColumnModel().getColumn(0).setMaxWidth(20);
        }
    }

