    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (Validacao.validarCamposObrigatorios(txtNome, txtTipo, txtPeso, txtIngrediente, txtQtdestoque)) {
        Produto p = new Produto();

        String nome = txtNome.getText();
        p.setNome(nome);
        p.setTipo(txtTipo.getText());
        p.setIngrediente(txtIngrediente.getText());

        String qtdestoqueString = txtQtdestoque.getText();
        int qtdestoque = Integer.parseInt(qtdestoqueString);
        p.setQtdestoque(qtdestoque);

        String pesoString = txtPeso.getText();
        double peso = Double.parseDouble(pesoString);
        p.setPeso(peso);

        if (codigo == 0) {
            boolean retorno = controlaProduto.salvar(p);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Salvo com sucesso");
                txtNome.setText("");
                txtTipo.setText("");
                txtIngrediente.setText("");
                txtPeso.setText("");
                txtQtdestoque.setText("");

                txtNome.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
            }
        } else {
            p.setId(codigo);
            boolean retorno = controlaProduto.editar(p);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Editado com sucesso");
                txtNome.setText("");
                txtTipo.setText("");
                txtIngrediente.setText("");
                txtPeso.setText("");
                txtQtdestoque.setText("");

                montaTabela();
                containerAbas.setSelectedIndex(1);
                codigo = 0;
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");

            }
        }
    }
    }//GEN-LAST:event_btnSalvarActionPerformed

