    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblProduto.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, " Selecione alguem caralho");

        } else {
            String idString = String.valueOf(tblProduto.getValueAt(tblProduto.getSelectedRow(), 0));
            int idproduto = Integer.parseInt(idString);

            Produto p = controlaProduto.recuperar(idproduto);

            if (p == null) {
                JOptionPane.showMessageDialog(null, "Registro não localizado!");
            } else {

                // preenche a variavel codigo que sera usada para salvar ou editar
                codigo = p.getId();

                // mudar a aba ativa para Cadastro
                containerAbas.setSelectedIndex(0);

                // coloca dados nos campos de interface
                txtNome.setText(p.getNome());
                txtTipo.setText(p.getTipo());
                txtIngrediente.setText(p.getIngrediente());
                txtPeso.setText(String.valueOf(p.getPeso()));
                txtQtdestoque.setText(String.valueOf(p.getQtdestoque()));

                txtNome.requestFocus();
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tblProduto.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, " Selecione alguem caralho");

        } else {
            String idString = String.valueOf(tblProduto.getValueAt(tblProduto.getSelectedRow(), 0));
            int idproduto = Integer.parseInt(idString);

            boolean retorno = controlaProduto.excluir(idproduto);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
                montaTabela();
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro ao excluir!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

