    private void montaTabela() {
        ArrayList<Funcionario> funcionarios = controlaFuncionario.recuperarTodos();
        if (funcionarios == null) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar Funcionarios");
        } else {
            tblFuncionario.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {

                        case 1:
                            return "Nome";
                        case 2:
                            return "Cargo";
                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 3;
                }

                @Override
                public int getRowCount() {
                    return funcionarios.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Funcionario f = funcionarios.get(rowIndex);

                    if (f != null) {
                        switch (columnIndex) {
                            case 0:
                                return f.getIdfuncionario();
                            case 1:
                                return f.getNome();
                            case 2:
                                return f.getCargo();

                        }

                    }

                    return "n/d";
                }
            });

            tblFuncionario.getColumnModel().getColumn(0).setMinWidth(80);
            tblFuncionario.getColumnModel().getColumn(0).setPreferredWidth(80);
            tblFuncionario.getColumnModel().getColumn(0).setMaxWidth(20);
        }
    }

