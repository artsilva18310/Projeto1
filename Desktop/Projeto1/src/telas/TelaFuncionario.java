    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (Validacao.validarCamposObrigatorios(txtNome, txtCargo)) {
        Funcionario f = new Funcionario();

        String nome = txtNome.getText();
        f.setNome(nome);
        f.setCargo(txtCargo.getText());

        if (codigo == 0) {
            boolean retorno = controlaFuncionario.salvar(f);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Salvo com sucesso");
                txtNome.setText("");
                txtCargo.setText("");

                txtNome.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
            }
        } else {
            f.setIdfuncionario(codigo);
            boolean retorno = controlaFuncionario.editar(f);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Editado com sucesso");
                txtNome.setText("");
                txtCargo.setText("");

                montaTabela();
                containerAbas.setSelectedIndex(1);
                codigo = 0;
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
            }
        }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
        if (tblFuncionario.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, " Selecione alguem caralho");

        } else {

            String idString = String.valueOf(tblFuncionario.getValueAt(tblFuncionario.getSelectedRow(), 0));
            int idfuncionario = Integer.parseInt(idString);

            Funcionario f = controlaFuncionario.recuperar(idfuncionario);

            if (f == null) {
                JOptionPane.showMessageDialog(null, "Registro não localizado!");
            } else {

                // preenche a variavel codigo que sera usada para salvar ou editar
                codigo = f.getIdfuncionario();

                // mudar a aba ativa para Cadastro
                containerAbas.setSelectedIndex(0);

                // coloca dados nos campos de interface
                txtNome.setText(f.getNome());
                txtCargo.setText(f.getCargo());

                txtNome.requestFocus();
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tblFuncionario.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, " Selecione alguem caralho");

        } else {
            String idfuncionarioString = String.valueOf(tblFuncionario.getValueAt(tblFuncionario.getSelectedRow(), 0));
            int idfuncionario = Integer.parseInt(idfuncionarioString);

            boolean retorno = controlaFuncionario.excluir(idfuncionario);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
                montaTabela();
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro ao excluir!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

