    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (Validacao.validarCamposObrigatorios(txtUsuario, txtSenha)) {
        String usuario = txtUsuario.getText();
        String senha = txtSenha.getText();
        boolean retorno = controlaUsuario.validaLogin(usuario, senha);
        if (retorno) {
            Usuario uLogado = controlaUsuario.recuperar(usuario);
            TelaPrincipal tp = new TelaPrincipal(uLogado);
            tp.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao logar, verifique a senha ou o usuario");

        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

