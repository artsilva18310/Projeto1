  private void montaTabela() {
        ArrayList<Usuario> usuarios = controlaUsuario.recuperarTodos();
        if (usuarios == null) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar Funcionarios");
        } else {
            tblUsuario.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {

                        case 1:
                            return "Usuario";
                        case 2:
                            return "Senha";
                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 3;
                }

                @Override
                public int getRowCount() {
                    return usuarios.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Usuario u = usuarios.get(rowIndex);

                    if (u != null) {
                        switch (columnIndex) {
                            case 0:
                                return u.getId();
                            case 1:
                                return u.getUsuario();
                            case 2:
                                return u.getSenha();

                        }

                    }

                    return "n/d";
                }
            });

            tblUsuario.getColumnModel().getColumn(0).setMinWidth(80);
            tblUsuario.getColumnModel().getColumn(0).setMaxWidth(20);
        }
    }

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblUsuario.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, " Selecione alguem caralho");

        } else {

            String idString = String.valueOf(tblUsuario.getValueAt(tblUsuario.getSelectedRow(), 0));
            int idusuario = Integer.parseInt(idString);

            Usuario u = controlaUsuario.recuperar(idusuario);

            if (u == null) {
                JOptionPane.showMessageDialog(null, "Registro n√£o localizado!");
            } else {

                // preenche a variavel codigo que sera usada para salvar ou editar
                codigo = u.getId();

                // mudar a aba ativa para Cadastro
                containerAbas.setSelectedIndex(0);

                // coloca dados nos campos de interface
                txtUsuario.setText(u.getUsuario());
                txtSenha.setText(u.getSenha());

                txtUsuario.requestFocus();
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tblUsuario.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, " Selecione alguem caralho");

        } else {
            String idusuarioString = String.valueOf(tblUsuario.getValueAt(tblUsuario.getSelectedRow(), 0));
            int idusuario = Integer.parseInt(idusuarioString);

            boolean retorno = controlaUsuario.excluir(idusuario);
            if (retorno) {
                JOptionPane.showMessageDialog(null, "Registro exclu√≠do com sucesso!");
                montaTabela();
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro ao excluir!");
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

