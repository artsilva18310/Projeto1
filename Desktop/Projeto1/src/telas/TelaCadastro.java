    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (Validacao.validarCamposObrigatorios(txtUsuario, txtSenha)) {

            Usuario u = new Usuario();

            String usuario = txtUsuario.getText();
            u.setUsuario(usuario);
            u.setSenha(txtSenha.getText());

            if (codigo == 0) {
                boolean retorno = controlaUsuario.salvar(u);
                if (retorno) {
                    JOptionPane.showMessageDialog(null, "Salvo com sucesso");
                    txtUsuario.setText("");
                    txtSenha.setText("");

                    txtUsuario.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
                }
            } else {
                u.setId(codigo);
                boolean retorno = controlaUsuario.editar(u);
                if (retorno) {
                    JOptionPane.showMessageDialog(null, "Editado com sucesso");
                    txtUsuario.setText("");
                    txtSenha.setText("");

                    montaTabela();
                    containerAbas.setSelectedIndex(1);
                    codigo = 0;
                } else {
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");

                }
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed


